package com.example.stevie.connection.model.bean;

public class Tag {
    private int id_tag;
    private String cod_tag;
    private String id_tag_frente;
    private String id_tag_tras;
    private String id_tag_direita;
    private String id_tag_esquerda;

    public int getId_tag() {
        return id_tag;
    }

    public void setId_tag(int id_tag) {
        this.id_tag = id_tag;
    }

    public String getId_tag_frente() {
        return id_tag_frente;
    }

    public void setId_tag_frente(String id_tag_frente) {
        this.id_tag_frente = id_tag_frente;
    }

    public String getId_tag_tras() {
        return id_tag_tras;
    }

    public void setId_tag_tras(String id_tag_tras) {
        this.id_tag_tras = id_tag_tras;
    }

    public String getId_tag_direita() {
        return id_tag_direita;
    }

    public void setId_tag_direita(String id_tag_direita) {
        this.id_tag_direita = id_tag_direita;
    }

    public String getId_tag_esquerda() {
        return id_tag_esquerda;
    }

    public void setId_tag_esquerda(String id_tag_esquerda) {
        this.id_tag_esquerda = id_tag_esquerda;
    }

    public String getCod_tag() {
        return cod_tag;
    }

    public void setCod_tag(String cod_tag) {
        this.cod_tag = cod_tag;
    }
}
