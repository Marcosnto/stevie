package database.connection.model.bean;

public class Regiao {
    private int id_regiao;
    private String nome_região;

    public int getId_regiao() {
        return id_regiao;
    }

    public void setId_regiao(int id_regiao) {
        this.id_regiao = id_regiao;
    }

    public String getNome_região() {
        return nome_região;
    }

    public void setNome_região(String nome_região) {
        this.nome_região = nome_região;
    }
}
